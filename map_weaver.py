    def loadBasemapFromFile(self, file_path):
        """Load a local .shp or zipped shapefile (Library tab / library folder)."""
        file_path = os.path.normpath(os.path.abspath(file_path))
        if not os.path.isfile(file_path):
            self.iface.messageBar().pushMessage(
                "Warning", f"File not found: {file_path}", level=Qgis.Warning
            )
            return
        ext = os.path.splitext(file_path)[1].lower()
        if ext == ".shp":
            self.getBasemap(file_path)
            return
        if ext == ".zip":
            self._add_layer_from_local_shapefile_zip(file_path)
            return
        self.iface.messageBar().pushMessage(
            "Warning", f"Unsupported file type: {file_path}", level=Qgis.Warning
        )

    def _add_layer_from_local_shapefile_zip(self, zip_path):
        try:
            with zipfile.ZipFile(zip_path, "r") as zf:
                shp_names = [n for n in zf.namelist() if n.lower().endswith(".shp")]
        except zipfile.BadZipFile as e:
            self.iface.messageBar().pushMessage("Warning", f"Invalid zip: {e}", level=Qgis.Warning)
            return
        if not shp_names:
            self.iface.messageBar().pushMessage(
                "Warning", "No shapefile (.shp) found in zip.", level=Qgis.Warning
            )
            return
        shp_inner = sorted(shp_names)[0]
        znorm = zip_path.replace("\\", "/")
        vsipath = "/vsizip/" + znorm + "/" + shp_inner.replace("\\", "/")
        layer_name = self.FileNamePlain(os.path.basename(shp_inner))
        vlayer = QgsVectorLayer(vsipath, layer_name, "ogr")
        if vlayer.isValid():
            QgsProject.instance().addMapLayer(vlayer)
            print(f"Successfully added from zip: {layer_name}")
            return

        def first_shp_under(root):
            for r, _, files in os.walk(root):
                for fn in files:
                    if fn.lower().endswith(".shp"):
                        return os.path.join(r, fn)
            return None

        filename_only = os.path.splitext(os.path.basename(zip_path))[0]
        extract_folder = os.path.join(os.path.dirname(zip_path), filename_only)
        shp_file = first_shp_under(extract_folder) if os.path.isdir(extract_folder) else None
        if shp_file is None:
            try:
                os.makedirs(extract_folder, exist_ok=True)
                self._safeExtractZip(zip_path, extract_folder)
            except Exception as e:
                self.iface.messageBar().pushMessage(
                    "Warning", f"Could not read zip: {e}", level=Qgis.Warning
                )
                return
            shp_file = first_shp_under(extract_folder)
        if shp_file:
            self.getBasemap(shp_file)
        else:
            self.iface.messageBar().pushMessage(
                "Warning", "Could not load a shapefile from zip.", level=Qgis.Warning
            )

    
    def _current_border_pen_style(self):
        """Pen style from cmbBorderStyle when populated (Designer / user); else radios."""
        dw = self.dockwidget
        if dw is None:
            return Qt.SolidLine
        combo = getattr(dw, "cmbBorderStyle", None)
        if combo is not None and combo.count() > 0:
            data = combo.currentData()
            if data is not None:
                try:
                    return int(data)
                except (TypeError, ValueError):
                    pass
        if dw.radDashLine.isChecked():
            return Qt.DashLine
        if dw.radSolidLine.isChecked():
            return Qt.SolidLine
        return Qt.SolidLine

    def _on_border_weight_changed(self, value):
        """No line + positive weight is invisible; bump style to Solid when weight is edited."""
        try:
            w = float(value)
        except (TypeError, ValueError):
            w = 0.0
        dw = self.dockwidget
        if dw is not None and w > 0:
            combo = getattr(dw, "cmbBorderStyle", None)
            if combo is not None and hasattr(combo, "currentData"):
                data = combo.currentData()
                try:
                    ps = int(data) if data is not None else None
                except (TypeError, ValueError):
                    ps = None
                if ps == int(Qt.NoPen):
                    idx = combo.findData(int(Qt.SolidLine))
                    if idx < 0:
                        idx = 0
                    combo.blockSignals(True)
                    try:
                        combo.setCurrentIndex(idx)
                    finally:
                        combo.blockSignals(False)
        self.SetSymbolBorders()

    def SetSymbolBorders(self):

        new_color = self.dockwidget.plhQWcolorbutton.color()
        brweight = float(self.dockwidget.dsbBorderWeight.value())

        brstyle = self._current_border_pen_style()
        if brweight == 0:
            brstyle = Qt.NoPen

        layer = self._layerForFormatApply()
        if layer is None:
            self.iface.messageBar().pushMessage(
                "Warning",
                "Select a layer in the panel (or set an active layer) to apply border settings.",
                level=Qgis.Warning,
            )
            return
        renderer = layer.renderer()
        sym = MapestryPluginDockWidget._renderer_primary_symbol(layer)
        if sym is None or sym.symbolLayerCount() < 1:
            return
        symbol_layer = sym.symbolLayer(0)

        if isinstance(symbol_layer, QgsSimpleLineSymbolLayer):
            symbol_layer.setPenStyle(brstyle)
            symbol_layer.setWidth(brweight)
        elif isinstance(symbol_layer, QgsSimpleFillSymbolLayer):
            symbol_layer.setStrokeStyle(brstyle)
            symbol_layer.setStrokeWidth(brweight)
        elif MapestryPluginDockWidget._is_simple_marker_symbol_layer(symbol_layer):
            symbol_layer.setStrokeStyle(brstyle)
            symbol_layer.setStrokeWidth(brweight)
        elif hasattr(renderer, "symbols"):
            symbol_layer.setStrokeStyle(brstyle)
            symbol_layer.setStrokeWidth(brweight)
        if new_color.isValid():
            # Simple line ink is color(), not strokeColor(); strokeColor is for fills/markers outlines.
            if isinstance(symbol_layer, QgsSimpleLineSymbolLayer):
                symbol_layer.setColor(new_color)
            else:
                symbol_layer.setStrokeColor(new_color)

        layer.triggerRepaint()
        iface.layerTreeView().refreshLayerSymbology(layer.id())

