    def _sync_pan_select_buttons_from_canvas_tool(self, tool):
        """Reflect canvas map tool on checkable Pan / Select buttons (Move Label clears both)."""
        dw = self.dockwidget
        if dw is None or sip.isdeleted(dw):
            return
        pan_btn = getattr(dw, "pbnPanMap", None)
        sel_btn = getattr(dw, "pbnSelectMap", None)
        if pan_btn is None or sel_btn is None:
            return
        if sip.isdeleted(pan_btn) or sip.isdeleted(sel_btn):
            return
        if self._map_tool_is_move_label_tool(tool):
            pan_on, sel_on = False, False
        elif self._map_tool_is_pan_tool(tool):
            pan_on, sel_on = True, False
        elif self._map_tool_is_select_features_tool(tool):
            pan_on, sel_on = False, True
        else:
            pan_on, sel_on = False, False
        pan_btn.blockSignals(True)
        sel_btn.blockSignals(True)
        try:
            pan_btn.setChecked(bool(pan_on))
            sel_btn.setChecked(bool(sel_on))
        finally:
            pan_btn.blockSignals(False)
            sel_btn.blockSignals(False)

    def _on_pbn_pan_toggled(self, checked):
        """Exclusive with Select: checked → Pan tool; unchecked → Select tool."""
        dw = self.dockwidget
        if dw is None:
            return
        pan = getattr(dw, "pbnPanMap", None)
        sel = getattr(dw, "pbnSelectMap", None)
        if pan is None:
            return
        if not checked:
            self._exclusive_check_select_when_pan_off(sel)
            self._activate_select_features_tool()
            return
        if sel is not None:
            sel.blockSignals(True)
            sel.setChecked(False)
            sel.blockSignals(False)
        self._activate_pan_map_tool()

