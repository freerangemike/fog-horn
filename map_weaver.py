    def _densestLayerVertexCenter(self, layer):
        """Return center point of the densest vertex bin."""
        if not isinstance(layer, QgsVectorLayer):
            return None
        extent = layer.extent()
        if extent is None:
            return None

        try:
            xmin = float(extent.xMinimum())
            ymin = float(extent.yMinimum())
            w = float(extent.width())
            h = float(extent.height())
        except Exception:
            # Some SIP bindings can return unexpected method descriptors in edge cases.
            # Fall back to safe centering behavior instead of crashing.
            return None

        if w <= 0 or h <= 0:
            return None

        cols = 24
        rows = 24
        counts = [[0 for _ in range(cols)] for _ in range(rows)]
        max_vertices = 50000
        seen = 0

        for f in layer.getFeatures():
            g = f.geometry()
            if g is None or g.isEmpty():
                continue
            for v in g.vertices():
                if seen >= max_vertices:
                    break
                x = v.x()
                y = v.y()
                cx = int((x - xmin) / w * cols)
                cy = int((y - ymin) / h * rows)
                if cx < 0:
                    cx = 0
                elif cx >= cols:
                    cx = cols - 1
                if cy < 0:
                    cy = 0
                elif cy >= rows:
                    cy = rows - 1
                counts[cy][cx] += 1
                seen += 1
            if seen >= max_vertices:
                break

        if seen == 0:
            return None

        best_x = 0
        best_y = 0
        best = -1
        for r in range(rows):
            for c in range(cols):
                if counts[r][c] > best:
                    best = counts[r][c]
                    best_x = c
                    best_y = r
        if best <= 0:
            return None

        center_x = xmin + (best_x + 0.5) * (w / cols)
        center_y = ymin + (best_y + 0.5) * (h / rows)
        return QgsPointXY(center_x, center_y)

    def _safeExtractZip(self, zip_path, extract_folder):
        """Extract ZIP while preventing path traversal (zip-slip)."""
        abs_target = os.path.abspath(extract_folder)
        with zipfile.ZipFile(zip_path, "r") as zip_ref:
            for member in zip_ref.infolist():
                member_path = os.path.abspath(os.path.join(abs_target, member.filename))
                if not (member_path == abs_target or member_path.startswith(abs_target + os.sep)):
                    raise ValueError(f"Unsafe path in zip: {member.filename}")
            zip_ref.extractall(abs_target)

