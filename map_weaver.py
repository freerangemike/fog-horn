    def load_online_shapefile(url, target_folder, layer_name):
        """
        Downloads a zipped shapefile from a URL, extracts it, and loads it into QGIS.

        :param url: URL of the zipped shapefile.
        :param target_folder: Local folder to save and extract the files.
        :param layer_name: Desired name for the layer in QGIS.
        """
        # Ensure the target folder exists
        if not os.path.exists(target_folder):
            os.makedirs(target_folder)

        # 1. Download the zipped file
        zip_path = os.path.join(target_folder, 'temp_shapefile.zip')
        print(f"Downloading {url} to {zip_path}...")
        try:
            response = requests.get(url)
            response.raise_for_status() # Check for bad request
            with open(zip_path, 'wb') as f:
                f.write(response.content)
            print("Download complete.")
        except requests.exceptions.RequestException as e:
            print(f"Error during download: {e}")
            return

        # 2. Extract the zip file
        with zipfile.ZipFile(zip_path, 'r') as zip_ref:
            zip_ref.extractall(target_folder)
        print(f"Files extracted to {target_folder}.")

        # 3. Find the .shp file in the extracted folder
        shp_file = None
        for root, dirs, files in os.walk(target_folder):
            for file in files:
                if file.endswith('.shp'):
                    shp_file = os.path.join(root, file)
                    break
            if shp_file:
                break

        if shp_file:
            # 4. Load the shapefile into QGIS
            # QgsVectorLayer(dataProviderUri, layerName, providerName)
            layer = QgsVectorLayer(shp_file, layer_name, 'ogr')

            if not layer.isValid():
                print("Layer failed to load!")
            else:
                QgsProject.instance().addMapLayer(layer)
                print(f"Layer '{layer_name}' loaded successfully into QGIS.")
        else:
            print("Could not find a .shp file in the extracted contents.")

        # Optional: Clean up the downloaded zip file
        if os.path.exists(zip_path):
            os.remove(zip_path)

