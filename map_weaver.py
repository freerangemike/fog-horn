    def _should_exit_move_label_mode(self, layer):
        """True when this click should leave move-label mode (Pan), not start setup for a new target."""
        if iface is None:
            return False
        canvas = iface.mapCanvas()
        if canvas is None:
            return False
        if not self._map_tool_is_move_label_tool(canvas.mapTool()):
            return False
        if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
            return False
        if iface.activeLayer() is not layer:
            return False
        if not hasattr(layer, "labelsEnabled") or not layer.labelsEnabled():
            return False
        return True

    def _activate_pan_map_tool(self):
        if iface is None:
            return
        pan_act = getattr(iface, "actionPan", None)
        if callable(pan_act):
            a = pan_act()
            if a is not None:
                a.trigger()
                return
        mw = iface.mainWindow()
        if mw is not None:
            pfn = getattr(mw, "pan", None)
            if callable(pfn):
                pfn()

    def _refresh_move_label_context(self, layer):
        """Notify QGIS that labeling/aux changed so toolbars refresh (Properties OK does this implicitly)."""
        if layer is None or sip.isdeleted(layer):
            return
        es = getattr(layer, "emitStyleChanged", None)
        if callable(es):
            es()
        if iface is not None:
            iface.setActiveLayer(layer)
        QCoreApplication.processEvents()
        mw = iface.mainWindow() if iface is not None else None
        self._sync_builtin_label_toolbar(mw, layer)

    def _sync_builtin_label_toolbar(self, mw, layer):
        """Re-run QgisApp’s layer-action refresh so built-in Labels toolbar enables (same as Layer Properties OK)."""
        if mw is None or layer is None or sip.isdeleted(layer):
            return
        try:
            QMetaObject.invokeMethod(
                mw,
                "activateDeactivateLayerRelatedActions",
                Qt.DirectConnection,
                Q_ARG(QgsMapLayer, layer),
            )
        except (TypeError, RuntimeError, AttributeError):
            try:
                QMetaObject.invokeMethod(
                    mw, "updateLabelToolButtons", Qt.DirectConnection
                )
            except (TypeError, RuntimeError, AttributeError):
                pass
        try:
            from qgis.gui import QgsLayerStylingWidget

            for w in mw.findChildren(QgsLayerStylingWidget):
                sig = getattr(w, "styleChanged", None)
                if sig is not None and hasattr(sig, "emit"):
                    sig.emit()
        except (ImportError, TypeError, RuntimeError, AttributeError):
            pass
        QCoreApplication.processEvents()

    @staticmethod
    def _invoke_move_label_map_tool(mw, layer):
        """Call QgisApp.moveLabel() — same as the Labels toolbar but does not depend on QAction.isEnabled()."""
        if mw is None or layer is None or sip.isdeleted(layer):
            return False
        if not isinstance(layer, QgsVectorLayer):
            return False
        if not hasattr(layer, "labelsEnabled") or not layer.labelsEnabled():
            return False
        fn = getattr(mw, "moveLabel", None)
        if not callable(fn):
            return False
        fn()
        return True

    def _try_trigger_move_label_action(self, mw, act, layer=None):
        if self._should_exit_move_label_mode(layer):
            self._activate_pan_map_tool()
            return
        if layer is not None:
            self._ensure_auxiliary_storage_for_move_labels(layer)
            self._refresh_move_label_context(layer)
        if self._invoke_move_label_map_tool(mw, layer):
            return
        if act is not None and act.isEnabled():
            act.trigger()
            return
        self.iface.messageBar().pushMessage(
            self.tr("Move labels"),
            self.tr(
                "That tool is not active yet. Select a vector layer with labeling (or diagrams/callouts) enabled."
            ),
            level=Qgis.Info,
        )

    @staticmethod
    def _guess_auxiliary_primary_key_field_index(layer):
        """Pick a join field for auxiliary storage (moved labels); prefer provider PK then common id columns."""
        if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
            return -1
        fields = layer.fields()
        n = fields.count()
        if n <= 0:
            return -1
        try:
            pk_attrs = layer.primaryKeyAttributes()
        except (AttributeError, TypeError):
            pk_attrs = []
        if pk_attrs:
            idx = int(pk_attrs[0])
            if 0 <= idx < n:
                return idx
        preferred = (
            "fid",
            "OBJECTID",
            "OBJECTID_1",
            "GEOID",
            "OGR_FID",
            "ogc_fid",
            "gml_id",
            "id",
            "name",
        )
        lower_to_idx = {}
        for i in range(n):
            nm = fields.at(i).name()
            if nm:
                lower_to_idx[nm.lower()] = i
        for cand in preferred:
            j = lower_to_idx.get(cand.lower())
            if j is not None:
                return j
        return 0

    def _auxiliary_join_field_ready(self, layer):
        if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
            return False
        aux = layer.auxiliaryLayer()
        if aux is None:
            return False
        ji = aux.joinInfo()
        if ji is None:
            return False
        tfn = ji.targetFieldName()
        return bool(tfn and str(tfn).strip())

    @staticmethod
    def _pal_position_xy_properties_for_auxiliary():
        """QgsPalLayerSettings.Property entries for label X/Y (required for Move Label + auxiliary)."""
        p = QgsPalLayerSettings
        prop = getattr(p, "Property", None)
        if prop is not None:
            px = getattr(prop, "PositionX", None)
            py = getattr(prop, "PositionY", None)
            if px is not None and py is not None:
                return px, py
        px = getattr(p, "PositionX", None)
        py = getattr(p, "PositionY", None)
        if px is not None and py is not None:
            return px, py
        return None, None

    def _ensure_pal_auxiliary_position_properties(self, layer):
        """Wire PositionX/Y to auxiliary fields (same as Labels tab applying data-defined positions)."""
        if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
            return
        if not layer.labelsEnabled() or layer.labeling() is None or layer.auxiliaryLayer() is None:
            return
        lab = layer.labeling()
        if MapestryPluginDockWidget._is_rule_based_labeling(lab):
            return
        px, py = self._pal_position_xy_properties_for_auxiliary()
        if px is None or py is None:
            return
        try:
            QgsAuxiliaryLayer.createProperty(px, layer, True)
            QgsAuxiliaryLayer.createProperty(py, layer, True)
        except (TypeError, AttributeError):
            try:
                QgsAuxiliaryLayer.createProperty(px, layer)
                QgsAuxiliaryLayer.createProperty(py, layer)
            except (TypeError, AttributeError):
                return
        layer.triggerRepaint()

