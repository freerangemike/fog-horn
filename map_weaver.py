    def CenterMapOnSelectedLayerGeometryCenter(self):
        """Center map canvas on densest vertex area of selected layer."""
        layer = None
        if self.dockwidget is not None and hasattr(self.dockwidget, "selectedLayerFromPanel"):
            layer = self.dockwidget.selectedLayerFromPanel()
        if layer is None:
            self.iface.messageBar().pushMessage("Warning", "No layer available in panel.", level=Qgis.Warning)
            return

        extent = layer.extent()
        if extent.isEmpty():
            self.iface.messageBar().pushMessage(
                "Warning",
                f"Layer '{layer.name()}' has no geometry extent.",
                level=Qgis.Warning
            )
            return

        target_center = self._densestLayerVertexCenter(layer)
        if target_center is None:
            target_center = extent.center()

        canvas = self.iface.mapCanvas()
        if hasattr(layer, "crs") and layer.crs().isValid():
            try:
                xform = QgsCoordinateTransform(
                    layer.crs(), canvas.mapSettings().destinationCrs(), QgsProject.instance()
                )
                target_center = xform.transform(target_center)
            except Exception:
                # Keep fallback in layer coordinates if transform fails.
                pass

        canvas = self.iface.mapCanvas()
        canvas.setCenter(target_center)
        canvas.refresh()

