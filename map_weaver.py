    def _wire_move_label_mode_highlight(self):
        """Highlight pbnMoveLabels while the canvas Move Label map tool is active (any activation path)."""
        if self._move_label_highlight_wired:
            return
        canvas = self.iface.mapCanvas() if self.iface else None
        if canvas is None:
            return
        # QgsMapCanvas uses mapToolSet (QGIS API); some builds only expose mapToolChanged.
        handler = self._on_canvas_map_tool_changed_move_highlight
        if hasattr(canvas, "mapToolSet"):
            canvas.mapToolSet.connect(handler)
        elif hasattr(canvas, "mapToolChanged"):
            canvas.mapToolChanged.connect(handler)
        else:
            self._move_label_highlight_wired = False
            return
        self._move_label_highlight_wired = True
        self._on_canvas_map_tool_changed_move_highlight()

    def _unwire_move_label_mode_highlight(self):
        if not self._move_label_highlight_wired:
            return
        self._move_label_highlight_wired = False
        canvas = self.iface.mapCanvas() if self.iface else None
        if canvas is None:
            return
        handler = self._on_canvas_map_tool_changed_move_highlight
        for name in ("mapToolSet", "mapToolChanged"):
            if hasattr(canvas, name):
                try:
                    getattr(canvas, name).disconnect(handler)
                except (TypeError, RuntimeError):
                    pass

    def _on_canvas_map_tool_changed_move_highlight(self, new_tool=None, old_tool=None):
        tool = new_tool
        if tool is None and self.iface is not None and self.iface.mapCanvas() is not None:
            tool = self.iface.mapCanvas().mapTool()
        self._apply_move_label_button_highlight(tool)
        self._sync_pan_select_buttons_from_canvas_tool(tool)

