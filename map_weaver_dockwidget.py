    def eventFilter(self, obj, event):
        """Keep layer tree focused on click so selection highlight stays active (blue)."""
        if hasattr(self, "layer_tree_view") and obj is self.layer_tree_view.viewport():
            if event.type() == QtCore.QEvent.MouseButtonPress:
                self.layer_tree_view.setFocus(Qt.MouseFocusReason)
            elif event.type() == QtCore.QEvent.MouseButtonDblClick:
                pos = event.pos()
                idx = self.layer_tree_view.indexAt(pos)
                if idx.isValid() and idx.column() == 0:
                    if not self._embeddedLayerControlsContainPos(self.layer_tree_view, idx, pos):
                        if self._showLayerPropertiesForTreeIndex(idx):
                            event.accept()
                            return True
        return super(MapestryPluginDockWidget, self).eventFilter(obj, event)

    @staticmethod
    def _embeddedLayerControlsContainPos(tv, index, pos):
        """Same hit-test as LayerTreeRowControlsDelegate (edit / label chips)."""
        if tv is None or not index.isValid():
            return False
        vr = tv.visualRect(index)
        size = 16
        spacing = 6
        margin = 8
        y = vr.y() + max(0, (vr.height() - size) // 2)
        labels_rect = QtCore.QRect(vr.right() - margin - size, y, size, size)
        edit_rect = QtCore.QRect(labels_rect.left() - spacing - size, y, size, size)
        return edit_rect.contains(pos) or labels_rect.contains(pos)

    def _showLayerPropertiesForTreeIndex(self, index):
        """Open QGIS Layer Properties for the map layer at this tree index."""
        if iface is None or not hasattr(self, "layer_tree_view"):
            return False
        node = self._layerTreeIndexToNode(self.layer_tree_view, index)
        if not isinstance(node, QgsLayerTreeLayer):
            return False
        layer = node.layer()
        if layer is None or sip.isdeleted(layer):
            return False
        iface.showLayerProperties(layer)
        return True

