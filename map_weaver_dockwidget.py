def _setup_center_view_button(btn):
    """Icon-only center-map control using graphics/centermap.svg (rasterized; QIcon(svg path) is unreliable)."""
    if btn is None:
        return
    path = os.path.normpath(_graphics_path("centermap.svg"))
    px = 14
    icon = QIcon()
    if os.path.isfile(path):
        try:
            from qgis.PyQt.QtSvg import QSvgRenderer
        except ImportError:
            from PyQt5.QtSvg import QSvgRenderer
        renderer = QSvgRenderer(path)
        if not renderer.isValid():
            try:
                with open(path, "rb") as fh:
                    renderer = QSvgRenderer(fh.read())
            except OSError:
                renderer = QSvgRenderer()
        if renderer.isValid():
            pm = QPixmap(px, px)
            pm.fill(QtCore.Qt.transparent)
            painter = QPainter(pm)
            renderer.render(painter, QtCore.QRectF(0, 0, px, px))
            painter.end()
            icon = QIcon(pm)
    if not icon.isNull():
        btn.setIcon(icon)
        btn.setText("")
        btn.setIconSize(QSize(px, px))
    else:
        btn.setText("C")
    # Size comes from Qt Designer (.ui); do not setFixedSize here or it overrides the UI.
    btn.setToolTip(
        QtCore.QCoreApplication.translate(
            "MapestryPluginDockWidget", "Center map on the selected layer"
        )
    )


class GlobeGeoQmlLogger(QtCore.QObject):
    """Forward globe debug lines from embedded QQuick to QgsMessageLog."""

    # Safety net if QML emits duplicate spin_probe lines (e.g. validate + timer overlap).
    _spin_probe_suppress_s = 35.0
    _spin_probe_key_re = re.compile(
        r"globe_geo spin_probe yaw=([-\d.]+) pitch=([-\d.]+) maxViewSeamParts=\d+ "
        r"(?:drawViewSeamSplit=\d+ )?"
        r"(?:polyIndex|worldPolyIndex)=(\d+) c=(.+)$"
    )
    _spin_probe_key_re_single = re.compile(
        r"globe_geo spin_probe yaw=([-\d.]+) pitch=([-\d.]+) maxViewSeamParts=1 "
        r"(?:drawViewSeamSplit=\d+ )?"
        r"drawPolys=(\d+)"
    )

    def __init__(self, parent=None):
        super().__init__(parent)
        self._spin_probe_last_mono_by_key = {}

    @pyqtSlot(str)
    def log(self, msg):
        try:
            msg = str(msg).strip()
            if "[map_weaver] globe_geo spin_probe" in msg:
                now = time.monotonic()
                m = self._spin_probe_key_re.search(msg)
                if m:
                    dedup_key = (
                        m.group(1) + "|" + m.group(2) + "|" + m.group(3) + "|" + m.group(4).strip()
                    )
                else:
                    m1 = self._spin_probe_key_re_single.search(msg)
                    dedup_key = (
                        m1.group(1) + "|" + m1.group(2) + "|1|" + m1.group(3)
                        if m1
                        else msg
                    )
                last = self._spin_probe_last_mono_by_key.get(dedup_key)
                if last is not None and (now - last) < self._spin_probe_suppress_s:
                    return
                self._spin_probe_last_mono_by_key[dedup_key] = now
                if len(self._spin_probe_last_mono_by_key) > 32:
                    cutoff = now - 180.0
                    self._spin_probe_last_mono_by_key = {
                        k: v
                        for k, v in self._spin_probe_last_mono_by_key.items()
                        if v >= cutoff
                    }
            QgsMessageLog.logMessage(msg, "MapWeaver Globe", Qgis.MessageLevel.Info)
        except Exception:
            pass


class LayerOpToolButton(QtWidgets.QToolButton):
    """Cloned layer-toolbar control; Filter Legend uses guarded mouse handling (event filters miss split-button hits on some platforms)."""

    def __init__(self, dock, parent=None):
        super().__init__(parent)
        self._map_weaver_dock = dock

    def _blockFilterLegendInteraction(self):
        d = self._map_weaver_dock
        if d is None or not d._filterLegendShouldBeDisabledForTreeSelection():
            return False
        # Do not rely on one detector; this must stay true even if one identity path misses.
        if d._operationButtonIsFilterLegend(self):
            return True
        action = d.layer_operation_action_map.get(self) if hasattr(d, "layer_operation_action_map") else None
        refs = getattr(d, "_filter_legend_action_refs", set())
        if action is not None and action in refs:
            return True
        src = d.layer_operation_source_button_map.get(self) if hasattr(d, "layer_operation_source_button_map") else None
        if src is not None:
            sb_on = (src.objectName() or "").lower().replace("_", "")
            if "filterlegend" in sb_on:
                return True
            sda = src.defaultAction()
            if sda is not None:
                sda_on = (sda.objectName() or "").lower().replace("_", "")
                if "filterlegend" in sda_on:
                    return True
        menu = d.layer_operation_menu_map.get(self) if hasattr(d, "layer_operation_menu_map") else None
        return d._menuLooksLikeFilterLegend(self, menu)

    def mousePressEvent(self, e):
        if self._blockFilterLegendInteraction():
            e.accept()
            if self._map_weaver_dock is not None:
                self._map_weaver_dock._hardBlockFilterLegendButton(self)
            return
        super().mousePressEvent(e)

    def mouseReleaseEvent(self, e):
        if self._blockFilterLegendInteraction():
            e.accept()
            return
        super().mouseReleaseEvent(e)

    def showMenu(self):
        # Arrow-side popup path for split buttons.
        if self._blockFilterLegendInteraction():
            if self._map_weaver_dock is not None:
                self._map_weaver_dock._hardBlockFilterLegendButton(self)
            return
        super().showMenu()


class RoundedLayerTreeView(QgsLayerTreeView):
    """Layer tree view clipped to rounded corners so selection paint cannot bleed out."""

    def __init__(self, parent=None, radius=13):
        super().__init__(parent)
        self._corner_radius = radius

    def _applyRoundedMask(self):
        rect = self.rect()
        if rect.isEmpty():
            return
        path = QtGui.QPainterPath()
        path.addRoundedRect(QtCore.QRectF(rect.adjusted(0, 0, -1, -1)), self._corner_radius, self._corner_radius)
        region = QtGui.QRegion(path.toFillPolygon().toPolygon())
        self.setMask(region)

    def resizeEvent(self, event):
        super().resizeEvent(event)
        self._applyRoundedMask()

    def showEvent(self, event):
        super().showEvent(event)
        self._applyRoundedMask()


class LayerTreeRowControlsDelegate(QtWidgets.QStyledItemDelegate):
    """Paint and handle per-layer edit/label controls in the embedded tree."""

    def __init__(self, dock, parent=None):
        super().__init__(parent)
        self._dock = dock
        self._label_icon = QIcon(_graphics_path("label_icon.png"))
        self._edit_icon = QIcon(_graphics_path("pencil_icon.png"))
        if self._label_icon.isNull():
            self._label_icon = QIcon(_graphics_path("swatchx.png"))
        if self._edit_icon.isNull():
            self._edit_icon = QgsApplication.getThemeIcon("/mActionToggleEditing.svg")

    def _layerFromIndex(self, index):
        if self._dock is None or not index.isValid():
            return None
        node = self._dock._layerTreeIndexToNode(self._dock.layer_tree_view, index)
        if not isinstance(node, QgsLayerTreeLayer):
            return None
        return node.layer()

    def _controlRects(self, option):
        size = 16
        spacing = 6
        margin = 8
        y = option.rect.y() + max(0, (option.rect.height() - size) // 2)
        labels_rect = QtCore.QRect(option.rect.right() - margin - size, y, size, size)
        edit_rect = QtCore.QRect(
            labels_rect.left() - spacing - size, y, size, size
        )
        return edit_rect, labels_rect

    def paint(self, painter, option, index):
        super().paint(painter, option, index)
        if index.column() != 0:
            return

        layer = self._layerFromIndex(index)
        if layer is None or sip.isdeleted(layer):
            return

        edit_rect, labels_rect = self._controlRects(option)
        style = option.widget.style() if option.widget is not None else QtWidgets.QApplication.style()

        for rect, is_checked, state_icon in (
            (edit_rect, bool(layer.isEditable()) if hasattr(layer, "isEditable") else False, self._edit_icon),
            (labels_rect, bool(layer.labelsEnabled()) if hasattr(layer, "labelsEnabled") else False, self._label_icon),
        ):
            cb = QtWidgets.QStyleOptionButton()
            cb.rect = rect
            cb.state = QtWidgets.QStyle.State_Enabled | (
                QtWidgets.QStyle.State_On if is_checked else QtWidgets.QStyle.State_Off
            )
            style.drawControl(QtWidgets.QStyle.CE_CheckBox, cb, painter)
            if state_icon is None or state_icon.isNull():
                continue
            icon_mode = QIcon.Normal if is_checked else QIcon.Disabled
            icon_pixmap = state_icon.pixmap(
                rect.size(), icon_mode, QIcon.On if is_checked else QIcon.Off
            )
            painter.save()
            if not is_checked:
                painter.setOpacity(0.55)
            painter.drawPixmap(rect, icon_pixmap)
            painter.restore()

    def editorEvent(self, event, model, option, index):
        if index.column() != 0 or self._dock is None:
            return super().editorEvent(event, model, option, index)
        if event.type() != QtCore.QEvent.MouseButtonRelease:
            return super().editorEvent(event, model, option, index)

        layer = self._layerFromIndex(index)
        if layer is None or sip.isdeleted(layer):
            return super().editorEvent(event, model, option, index)

        edit_rect, labels_rect = self._controlRects(option)
        pos = event.pos()
        if edit_rect.contains(pos):
            self._dock._toggleLayerEditingFromRow(layer)
            if option.widget is not None:
                option.widget.viewport().update()
            return True
        if labels_rect.contains(pos):
            self._dock._toggleLayerLabelsFromRow(layer)
            if option.widget is not None:
                option.widget.viewport().update()
            return True
        return super().editorEvent(event, model, option, index)


class MarkerSizeDoubleSpinBox(QtWidgets.QDoubleSpinBox):
    """Renders EMPTY sentinel as blank text (Fusion/QSS often ignores specialValueText on doubles)."""

    def __init__(self, empty_sentinel=-1.0, parent=None):
        super().__init__(parent)
        self._empty_sentinel = float(empty_sentinel)

    def textFromValue(self, value):
        try:
            if abs(float(value) - self._empty_sentinel) < 1e-9:
                return ""
        except (TypeError, ValueError):
            pass
        return super().textFromValue(value)

    def valueFromText(self, text):
        if text is None:
            return self._empty_sentinel
        s = str(text).strip()
        if not s:
            return self._empty_sentinel
        return super().valueFromText(text)

    def stepBy(self, steps):
        v = float(self.value())
        if steps > 0 and abs(v - self._empty_sentinel) < 1e-9:
            self.setValue(0.0)
            return
        if steps < 0 and abs(v - 0.0) < 1e-9:
            self.setValue(self._empty_sentinel)
            return
        super().stepBy(steps)


