    def _operationButtonTooltip(self, action, button, menu=None, source_button=None):
        """Tooltip fallback for split buttons where QAction text/tooltip can be blank."""
        candidates = [
            action.toolTip() if action is not None else "",
            action.text() if action is not None else "",
            source_button.toolTip() if source_button is not None else "",
            source_button.text() if source_button is not None else "",
            button.toolTip() if button is not None else "",
        ]
        for c in candidates:
            if c and c.strip():
                return c.strip()
        if menu is not None:
            for ma in menu.actions():
                mt = (ma.text() or "").strip()
                if mt:
                    return mt
        return ""

    def _triggerLayerOperationFromSender(self):
        """Run mapped layer operation action against embedded panel selection."""
        button = self.sender()
        action = self.layer_operation_action_map.get(button)
        if action is None:
            return
        self._syncMainLayerSelectionForActions()
        is_fl = self._operationButtonIsFilterLegend(button) or action in getattr(
            self, "_filter_legend_action_refs", set()
        )
        if is_fl and self._filterLegendShouldBeDisabledForTreeSelection():
            self._hardBlockFilterLegendButton(button)
            return
        action_label = " ".join(
            [action.text() or "", action.toolTip() or "", action.statusTip() or "", action.objectName() or ""]
        )
        remove_by_text = (
            "Remove Layer" in action_label
            or "Remove Group" in action_label
            or "Remove Legend" in action_label
            or "legend entry" in action_label.lower()
        )
        on = (action.objectName() or "").lower()
        remove_by_name = any(
            token in on
            for token in (
                "removelayer",
                "removegroup",
                "removelegend",
                "remove_layer",
                "remove_group",
                "removegrouporlayer",
            )
        )
        if remove_by_text or remove_by_name:
            # Use the embedded view default action first so multi-selection in this panel
            # is applied directly (matches built-in remove behavior and dialogs).
            if hasattr(self, "layer_tree_view") and self.layer_tree_view is not None:
                da = self.layer_tree_view.defaultActions() if hasattr(self.layer_tree_view, "defaultActions") else None
                if da is not None and hasattr(da, "removeGroupOrLayer"):
                    da.removeGroupOrLayer()
                    return
            # Fallback to main panel default action.
            main_layer_view = iface.layerTreeView() if iface is not None else None
            if main_layer_view is not None:
                da = main_layer_view.defaultActions()
                if da is not None and hasattr(da, "removeGroupOrLayer"):
                    da.removeGroupOrLayer()
                    return
            action.trigger()
            return
        source_button = self.layer_operation_source_button_map.get(button)
        if source_button is not None:
            # For valid layer selection, use the original toolbar button behavior for Filter Legend main-click.
            # Invalid selections are already blocked above by _filterLegendShouldBeDisabledForTreeSelection().
            source_button.click()
            return
        action.trigger()

    def _syncOperationButtonState(self, action, button):
        """Mirror QAction state to cloned toolbar button."""
        button.setEnabled(action.isEnabled())
        button.setVisible(action.isVisible())
        button.setCheckable(action.isCheckable())
        if action.isCheckable():
            button.setChecked(action.isChecked())
        action_icon = action.icon()
        if not action_icon.isNull():
            button.setIcon(action_icon)
        else:
            fallback_icon = self.layer_operation_icon_fallback.get(button)
            if fallback_icon is not None and not fallback_icon.isNull():
                button.setIcon(fallback_icon)
        menu = self.layer_operation_menu_map.get(button)
        source_button = self.layer_operation_source_button_map.get(button)
        tooltip = self._operationButtonTooltip(action, button, menu, source_button)
        button.setToolTip(tooltip)

        # Filter Legend: only valid for layer entries — not groups/subgroups.
        is_filter_btn = self._operationButtonIsFilterLegend(button)
        if not is_filter_btn and action in getattr(self, "_filter_legend_action_refs", set()):
            is_filter_btn = True
        if not is_filter_btn:
            label = " ".join(
                [
                    tooltip or "",
                    action.text() or "",
                    action.statusTip() or "",
                    action.objectName() or "",
                ]
            )
            menu = self.layer_operation_menu_map.get(button)
            if menu is not None:
                for menu_action in menu.actions():
                    label += " " + (menu_action.text() or "")
                    label += " " + (menu_action.objectName() or "")
            blob = label.lower().replace(" ", "").replace("_", "")
            is_filter_btn = (
                "Filter Legend by Expression" in label
                or "FilterLegend" in label
                or "mActionFilterLegend" in label
                or ("filterlegend" in blob and "expression" in label.lower())
            )
        if not is_filter_btn:
            m = self.layer_operation_menu_map.get(button)
            if m is not None:
                is_filter_btn = self._menuLooksLikeFilterLegend(button, m)
        if not is_filter_btn:
            m = self.layer_operation_menu_map.get(button)
            if self._isAnonymousFilterLegendSignature(button, action, m):
                is_filter_btn = True
        if is_filter_btn:
            self.layer_operation_filter_legend_buttons.add(button)
            self._filter_legend_action_refs.add(action)
            self._applyFilterLegendSplitButtonState(button, action)

    def _applyFilterLegendSplitButtonState(self, button, action):
        """Disable Filter Legend for groups while preserving split-button layout/arrow."""
        menu_ref = self.layer_operation_menu_map.get(button)
        saved_mode = self.layer_operation_popup_mode.get(
            button, QtWidgets.QToolButton.MenuButtonPopup
        )
        if self._filterLegendShouldBeDisabledForTreeSelection():
            if menu_ref is not None and button.menu() is None:
                button.setMenu(menu_ref)
            button.setPopupMode(saved_mode)
            button.setEnabled(False)
            button.setDown(False)
            button.setStyleSheet("QToolButton { opacity: 0.45; }")
            return
        if menu_ref is not None and button.menu() is None:
            button.setMenu(menu_ref)
        button.setPopupMode(saved_mode)
        button.setEnabled(action.isEnabled())
        button.setStyleSheet("")

    def _toolbarActionIsFilterLegend(self, action, source_button):
        """Detect the built-in Filter Legend by Expression toolbar control."""
        on = (action.objectName() or "").lower().replace("_", "")
        if "mactionfilterlegend" in on:
            return True
        if source_button is not None:
            sb_on = (source_button.objectName() or "").lower().replace("_", "")
            if "filterlegend" in sb_on:
                return True
            sda = source_button.defaultAction()
            if sda is not None:
                sda_on = (sda.objectName() or "").lower().replace("_", "")
                if "mactionfilterlegend" in sda_on or "filterlegend" in sda_on:
                    return True
        parts = [
            action.text() or "",
            action.toolTip() or "",
            action.statusTip() or "",
            action.objectName() or "",
        ]
        if source_button is not None and source_button.menu() is not None:
            for ma in source_button.menu().actions():
                parts.extend(
                    [ma.text() or "", ma.toolTip() or "", ma.objectName() or ""]
                )
        blob = " ".join(parts).lower()
        compact = blob.replace(" ", "").replace("_", "")
        if "filterlegend" in compact and "expression" in blob:
            return True
        if "mactionfilterlegend" in compact:
            return True
        if "filter" in blob and "legend" in blob and "expression" in blob:
            return True
        return False

    def _layerTreeIndexToNode(self, tv, index):
        """Map a view index to QgsLayerTreeNode (unwraps QAbstractProxyModel if present)."""
        if not index.isValid():
            return None
        model = tv.model()
        idx = QtCore.QModelIndex(index)
        while model is not None:
            if hasattr(model, "mapToSource"):
                try:
                    src = model.mapToSource(idx)
                except Exception:
                    return None
                if not src.isValid():
                    return None
                idx = src
                model = model.sourceModel()
                continue
            break
        if model is not None and hasattr(model, "index2node"):
            return model.index2node(idx)
        return None

    def _viewIndexForNode(self, tv, node):
        """Map a layer-tree node to an index valid for the given view."""
        if tv is None or node is None or not hasattr(self, "layer_tree_model"):
            return QtCore.QModelIndex()
        src_idx = self.layer_tree_model.node2index(node)
        if not src_idx.isValid():
            return QtCore.QModelIndex()
        model = tv.model()
        if model is None:
            return QtCore.QModelIndex()
        if model is self.layer_tree_model:
            return src_idx

        # Build proxy chain down to the source model, then map forward.
        chain = []
        cur_model = model
        while cur_model is not None and cur_model is not self.layer_tree_model:
            if not hasattr(cur_model, "sourceModel") or not hasattr(cur_model, "mapFromSource"):
                return QtCore.QModelIndex()
            chain.append(cur_model)
            try:
                cur_model = cur_model.sourceModel()
            except Exception:
                return QtCore.QModelIndex()
        if cur_model is not self.layer_tree_model:
            return QtCore.QModelIndex()

        mapped = src_idx
        for proxy in reversed(chain):
            try:
                mapped = proxy.mapFromSource(mapped)
            except Exception:
                return QtCore.QModelIndex()
            if not mapped.isValid():
                return QtCore.QModelIndex()
        return mapped

    def _selectedNodesFromView(self, tv):
        """Return selected nodes in a model-agnostic form."""
        if tv is None:
            return []
        sm = tv.selectionModel()
        if sm is None:
            return []
        nodes = []
        seen = set()
        for idx in sm.selectedRows():
            node = self._layerTreeIndexToNode(tv, idx)
            if node is None:
                continue
            key = id(node)
            if key in seen:
                continue
            seen.add(key)
            nodes.append(node)
        return nodes

    def _filterLegendShouldBeDisabledForTreeSelection(self):
        """Disable when selection is empty or includes any group (not only map layers)."""
        if not hasattr(self, "layer_tree_view") or self.layer_tree_view is None:
            return True
        tv = self.layer_tree_view
        sm = tv.selectionModel()
        if sm is None:
            return True
        if tv.model() is None:
            return True

        # Use the same rows the user selected — currentNode() can lag behind and wrongly
        # point at a child layer while a group row is selected.
        indexes = list(sm.selectedRows())
        if not indexes:
            cur = sm.currentIndex()
            if cur.isValid():
                indexes = [cur]
        if not indexes:
            return True

        need_fallback = False
        for idx in indexes:
            if not idx.isValid():
                return True
            n = self._layerTreeIndexToNode(tv, idx)
            if n is None:
                need_fallback = True
                break
            if not isinstance(n, QgsLayerTreeLayer):
                return True
        if not need_fallback:
            return False

        nodes = list(tv.selectedNodes()) if hasattr(tv, "selectedNodes") else []
        if not nodes and hasattr(tv, "currentNode"):
            c2 = tv.currentNode()
            if c2 is not None:
                nodes = [c2]
        if not nodes:
            return True
        for n in nodes:
            if not isinstance(n, QgsLayerTreeLayer):
                return True
        return False

    def selectedLayerFromPanel(self, mutate_selection=True):
        """Return currently selected map layer from embedded layer panel.

        When ``mutate_selection`` is True (default), selecting a group navigates the tree to the
        first child layer. Callers that run from selection-model callbacks must pass False to
        avoid re-entrant selection updates.
        """
        if not hasattr(self, "layer_tree_view") or self.layer_tree_view is None:
            return None

        selected_layers = self.layer_tree_view.selectedLayers()
        if selected_layers:
            return selected_layers[0]

        # If a group/subgroup is selected, use the first layer inside that node.
        node_candidates = []
        if hasattr(self.layer_tree_view, "currentNode"):
            cn = self.layer_tree_view.currentNode()
            if cn is not None:
                node_candidates.append(cn)
        if hasattr(self.layer_tree_view, "selectedNodes"):
            for n in self.layer_tree_view.selectedNodes():
                if n is not None and n not in node_candidates:
                    node_candidates.append(n)
        for node in node_candidates:
            if isinstance(node, QgsLayerTreeLayer):
                lyr = node.layer()
                if lyr is not None:
                    return lyr
            if hasattr(node, "findLayers"):
                nested = node.findLayers()
                if nested:
                    first_node = nested[0]
                    first_layer = first_node.layer()
                    if (
                        mutate_selection
                        and first_layer is not None
                        and hasattr(self, "layer_tree_model")
                    ):
                        first_index = self.layer_tree_model.node2index(first_node)
                        if first_index.isValid():
                            sm = self.layer_tree_view.selectionModel()
                            if sm is not None:
                                self._expandParentsForIndex(first_index)
                                sm.setCurrentIndex(
                                    first_index,
                                    QtCore.QItemSelectionModel.ClearAndSelect
                                    | QtCore.QItemSelectionModel.Rows,
                                )
                                self.layer_tree_view.scrollTo(first_index)
                            if hasattr(self.layer_tree_view, "setCurrentNode"):
                                self.layer_tree_view.setCurrentNode(first_node)
                    if first_layer is not None:
                        return first_layer

        current_layer = self.layer_tree_view.currentLayer()
        if current_layer is not None:
            return current_layer

        layer_nodes = QgsProject.instance().layerTreeRoot().findLayers()
        if layer_nodes:
            first_node = layer_nodes[0]
            first_layer = first_node.layer()
            if mutate_selection and first_layer is not None and hasattr(self, "layer_tree_model"):
                first_index = self.layer_tree_model.node2index(first_node)
                if first_index.isValid():
                    selection_model = self.layer_tree_view.selectionModel()
                    if selection_model is not None:
                        self._expandParentsForIndex(first_index)
                        selection_model.setCurrentIndex(
                            first_index,
                            QtCore.QItemSelectionModel.ClearAndSelect | QtCore.QItemSelectionModel.Rows
                        )
                        self.layer_tree_view.scrollTo(first_index)
                    if hasattr(self.layer_tree_view, "setCurrentNode"):
                        self.layer_tree_view.setCurrentNode(first_node)
            return first_layer

        return None

    def _expandParentsForIndex(self, index):
        if not index.isValid() or not hasattr(self, "layer_tree_view"):
            return
        p = index.parent()
        while p.isValid():
            self.layer_tree_view.setExpanded(p, True)
            p = p.parent()

