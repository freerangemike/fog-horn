        tf.setEnabled(ok)
        tip = ""
        if not ok:
            tip = self.tr(
                "Select a layer that supports symbology (for example a vector layer) to enable these controls."
            )
        tf.setToolTip(tip)
        # Sync after enabling so QgsColorButton accepts symbology colors while widgets are enabled.
        self._syncSymbologyColorButtonsFromSelection()
        self._syncLabelFieldComboFromLayer(self._layerTargetForFormatTab())
        self._syncLabelFontComboFromLayer(self._layerTargetForFormatTab())
        self._syncLabelBiuButtonsFromLayer(self._layerTargetForFormatTab())

