    @staticmethod
    def _renderer_primary_symbol(layer):
        """
        Symbol used for quick UI sync: prefer typed renderer API (reliable in PyQGIS), then fallbacks.
        """
        if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
            return None
        r = layer.renderer()
        if r is None:
            return None
        if isinstance(r, QgsSingleSymbolRenderer):
            sym = r.symbol()
            if sym is not None and getattr(sym, "symbolLayerCount", lambda: 0)() > 0:
                return sym
        if isinstance(r, QgsCategorizedSymbolRenderer):
            for cat in r.categories():
                s = cat.symbol()
                if s is not None and getattr(s, "symbolLayerCount", lambda: 0)() > 0:
                    return s
        if isinstance(r, QgsGraduatedSymbolRenderer):
            for rg in r.ranges():
                s = rg.symbol()
                if s is not None and getattr(s, "symbolLayerCount", lambda: 0)() > 0:
                    return s
        sym_fn = getattr(r, "symbol", None)
        if callable(sym_fn):
            sym = sym_fn()
            if sym is not None and getattr(sym, "symbolLayerCount", lambda: 0)() > 0:
                return sym
        return None

