    def _toggleLayerLabelsFromRow(self, layer):
        if layer is None or sip.isdeleted(layer):
            return
        if not hasattr(layer, "labelsEnabled") or not hasattr(layer, "setLabelsEnabled"):
            return
        turn_on = not layer.labelsEnabled()
        new_label_field = None
        if turn_on and layer.type() == QgsMapLayer.VectorLayer:
            labeling = layer.labeling() if hasattr(layer, "labeling") else None
            if labeling is None:
                # First-time labels toggle: create a minimal labeling config so the
                # enabled flag has a visible effect immediately.
                fields = layer.fields() if hasattr(layer, "fields") else None
                n_fields = fields.count() if fields is not None and hasattr(fields, "count") else 0
                if n_fields > 0:
                    preferred_field_name = None
                    for i in range(n_fields):
                        candidate = fields.at(i).name()
                        if candidate and candidate.lower() == "name":
                            preferred_field_name = candidate
                            break
                    if preferred_field_name is None:
                        preferred_field_name = fields.at(0).name()
                    settings = QgsPalLayerSettings()
                    settings.enabled = True
                    settings.fieldName = preferred_field_name
                    text_format = QgsTextFormat()
                    settings.setFormat(text_format)
                    layer.setLabeling(QgsVectorLayerSimpleLabeling(settings))
                    new_label_field = preferred_field_name

        layer.setLabelsEnabled(turn_on)
        layer.triggerRepaint()
        layer_id = layer.id()
        cache = getattr(self, "_last_known_label_field_by_layer_id", None)
        if isinstance(cache, dict):
            if new_label_field:
                cache[layer_id] = str(new_label_field)
            elif not turn_on:
                cache.pop(layer_id, None)

        # refreshLayerSymbology + tree repaints + format sync must not run synchronously from the
        # embedded layer-tree delegate: both trees share the project root, and re-entering
        # QgsLayerTreeView from this handler has caused native heap crashes on Windows.
        def _deferred_label_toggle_ui():
            if sip.isdeleted(self):
                return
            lyr = QgsProject.instance().mapLayer(layer_id)
            if iface is not None and iface.layerTreeView() is not None:
                if lyr is not None and not sip.isdeleted(lyr):
                    iface.layerTreeView().refreshLayerSymbology(layer_id)
            self._refreshLayerTreePaints()
            self._refreshFormatControlsEnabled()

        QtCore.QTimer.singleShot(0, _deferred_label_toggle_ui)
