    def _probeQmlFile(self, qml_path):
        if QQuickWidget is None:
            return False, "QQuickWidget unavailable"
        if not os.path.exists(qml_path):
            return False, f"missing file: {os.path.basename(qml_path)}"
        try:
            probe = QQuickWidget(self.plhGlobePanel)
            probe.setResizeMode(QQuickWidget.SizeRootObjectToView)
            probe.setClearColor(QtGui.QColor("#ffffff"))
            probe.setSource(QUrl.fromLocalFile(qml_path))
            if probe.status() == QQuickWidget.Error:
                errs = probe.errors()
                msg = errs[0].toString() if errs else "Unknown QML error"
                probe.deleteLater()
                return False, msg
            probe.deleteLater()
            return True, "ok"
        except Exception as e:
            return False, str(e)

