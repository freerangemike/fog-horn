    def _syncLabelBiuButtonsFromLayer(self, layer):
        """Match B / I / U toggles to QgsTextFormat font style for single-label layers."""
        buttons = (
            getattr(self, "pushButton", None),
            getattr(self, "pushButton_2", None),
            getattr(self, "pushButton_3", None),
        )
        if all(x is None or sip.isdeleted(x) for x in buttons):
            return

        def _block_all(block):
            for b in buttons:
                if b is not None and not sip.isdeleted(b):
                    b.blockSignals(block)

        _block_all(True)
        try:
            dis_tip = self.tr("Enable labels on this layer to edit text style.")
            rb_tip = self.tr("Label text style is only editable for single-label layers (not rule-based).")
            if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
                for b in buttons:
                    if b is not None and not sip.isdeleted(b):
                        b.setEnabled(False)
                        b.setChecked(False)
                        b.setToolTip(dis_tip)
                return
            lab = layer.labeling() if hasattr(layer, "labeling") else None
            if MapestryPluginDockWidget._is_rule_based_labeling(lab):
                for b in buttons:
                    if b is not None and not sip.isdeleted(b):
                        b.setEnabled(False)
                        b.setChecked(False)
                        b.setToolTip(rb_tip)
                return
            st = MapestryPluginDockWidget._pal_settings_from_layer_labeling(layer)
            if st is None:
                for b in buttons:
                    if b is not None and not sip.isdeleted(b):
                        b.setEnabled(False)
                        b.setChecked(False)
                        b.setToolTip(dis_tip)
                return
            try:
                tf = st.format()
            except Exception:
                for b in buttons:
                    if b is not None and not sip.isdeleted(b):
                        b.setEnabled(False)
                        b.setChecked(False)
                return
            fnt = tf.font()
            states = (fnt.bold(), fnt.italic(), fnt.underline())
            tips = (
                self.tr("Bold label text"),
                self.tr("Italic label text"),
                self.tr("Underline label text"),
            )
            for b, on, tip in zip(buttons, states, tips):
                if b is None or sip.isdeleted(b):
                    continue
                b.setEnabled(True)
                b.setChecked(bool(on))
                b.setToolTip(tip)
        finally:
            _block_all(False)

