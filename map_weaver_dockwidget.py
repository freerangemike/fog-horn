    def _wrapPillSpinBoxes(self):
        """Replace Weight / Size spins with pill + painted arrows (Fusion+QSS breaks native steppers)."""
        from .southneedle import PillDoubleSpinBox

        for attr in ("dsbBorderWeight", "dsbBorderWeight_5"):
            spin = getattr(self, attr, None)
            if spin is None or sip.isdeleted(spin):
                continue
            if isinstance(spin, PillDoubleSpinBox):
                continue
