    @staticmethod
    def _label_want_from_referenced_fields(st, layer):
        """When fieldName is empty/unparseable, single referenced attribute is the label source."""
        if st is None or layer is None or sip.isdeleted(layer):
            return None
        ms = QgsMapSettings()
        ms.setDestinationCrs(layer.crs())
        ex = layer.extent()
        if ex is None or ex.isEmpty():
            return None
        ms.setExtent(ex)
        ms.setLayers([layer])
        ms.setOutputSize(QSize(256, 256))
        try:
            ctx = QgsRenderContext.fromMapSettings(ms)
            refs = st.referencedFields(ctx)
        except Exception:
            return None
        flds = layer.fields()
        if flds is None or flds.count() == 0:
            return None
        resolved = []
        for r in refs:
            idx = flds.lookupField(r)
            if idx >= 0:
                resolved.append(flds.at(idx).name())
        if len(resolved) == 1:
            return resolved[0]
        return None

    @staticmethod
    def _single_field_name_from_label_expression(expr):
        """If the label value is a plain field reference, return its name; else None."""
        if expr is None:
            return None
        s = str(expr).strip()
        if not s:
            return None
        m = re.match(r'^"([^"\\]+)"\s*$', s)
        if m:
            return m.group(1)
        m = re.match(r"^'([^'\\]+)'\s*$", s)
        if m:
            return m.group(1)
        return None

    @staticmethod
    def _canonical_field_name(layer, name_or_candidate):
        """Return layer field name as in QgsFields if candidate matches a column, else None."""
        if layer is None or sip.isdeleted(layer) or name_or_candidate is None:
            return None
        flds = layer.fields()
        if flds is None or flds.count() == 0:
            return None
        s = str(name_or_candidate).strip()
        if not s:
            return None
        idx = flds.lookupField(s)
        if idx >= 0:
            return flds.at(idx).name()
        return None

    @staticmethod
    def _label_field_name_from_settings_string(layer, raw):
        """
        QgsPalLayerSettings.fieldName() is either a column name or an expression string.
        QGIS often sets isExpression=True even for a single unquoted field — resolve via QgsFields.
        """
        if raw is None or layer is None or sip.isdeleted(layer):
            return None
        s = str(raw).strip()
        if not s:
            return None
        flds = layer.fields()
        if flds is None or flds.count() == 0:
            return None
        parsed = MapestryPluginDockWidget._single_field_name_from_label_expression(s)
        if parsed:
            return MapestryPluginDockWidget._canonical_field_name(layer, parsed)
        c = MapestryPluginDockWidget._canonical_field_name(layer, s)
        if c:
            return c
        if len(s) >= 2 and s[0] == s[-1] and s[0] in ('"', "'"):
            return MapestryPluginDockWidget._canonical_field_name(layer, s[1:-1])
        # e.g. expression "REGION" (string literal) or bare column ref in expression grammar
        ast_field = MapestryPluginDockWidget._field_name_from_expression_ast(layer, s)
        if ast_field:
            return ast_field
        return None

    @staticmethod
    def _field_name_from_expression_ast(layer, s):
        """If fieldName() is a QgsExpression that is only a column ref or string literal matching a field."""
        if not s or layer is None or sip.isdeleted(layer):
            return None
        try:
            from qgis.core import (
                QgsExpression,
                QgsExpressionNodeColumnRef,
                QgsExpressionNodeLiteral,
            )
        except ImportError:
            return None
        e = QgsExpression(s)
        if e.hasParserError():
            return None
        n = e.rootNode()
        if n is None:
            return None
        if isinstance(n, QgsExpressionNodeColumnRef):
            return MapestryPluginDockWidget._canonical_field_name(layer, n.name())
        if isinstance(n, QgsExpressionNodeLiteral):
            val = n.value()
            if isinstance(val, str):
                return MapestryPluginDockWidget._canonical_field_name(layer, val)
        return None

    @staticmethod
    def _label_column_name_from_layer(layer):
        """Current single-label field/column name, or None if unknown."""
        if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
            return None
        st = MapestryPluginDockWidget._pal_settings_from_layer_labeling(layer)
        if st is None:
            return None
        try:
            fn = st.fieldName()
        except Exception:
            fn = ""
        if fn is None:
            fn = ""
        if not isinstance(fn, str):
            fn = str(fn)
        fn = fn.strip()
        want = None
        if fn:
            want = MapestryPluginDockWidget._label_field_name_from_settings_string(layer, fn)
        if not want:
            want = MapestryPluginDockWidget._label_want_from_referenced_fields(st, layer)
        return want

    def _syncLabelFieldComboFromLayer(self, layer):
        """Populate label column combo from vector fields; selection matches Labels → Value for single labels."""
        cmb = getattr(self, "cmbLabelField", None)
        if cmb is None or sip.isdeleted(cmb):
            return
        cmb.blockSignals(True)
        try:
            cmb.clear()
            cmb.setToolTip(
                self.tr("Attribute column used for single labels (same as Labels → Value).")
            )
            if layer is None or sip.isdeleted(layer) or not isinstance(layer, QgsVectorLayer):
                cmb.setEnabled(False)
                return
            lab = layer.labeling() if hasattr(layer, "labeling") else None
            if MapestryPluginDockWidget._is_rule_based_labeling(lab):
                cmb.setEnabled(False)
                cmb.setToolTip(
                    self.tr("Label column is only editable for single-label layers (not rule-based).")
                )
                return
            flds = layer.fields()
            if flds is None or flds.count() == 0:
                cmb.setEnabled(False)
                return
            for i in range(flds.count()):
                f = flds.at(i)
                nm = f.name()
                cmb.addItem(nm, nm)
            want = MapestryPluginDockWidget._label_column_name_from_layer(layer)
            cache = getattr(self, "_last_known_label_field_by_layer_id", None)
            if not want and isinstance(cache, dict):
                want = cache.get(layer.id())
            cmb.setEnabled(True)
            if want:
                idx = cmb.findData(want)
                if idx < 0:
                    idx = cmb.findText(want)
                if idx < 0:
                    wlow = str(want).lower()
                    for i in range(cmb.count()):
                        d = cmb.itemData(i)
                        t = cmb.itemText(i)
                        if d is not None and str(d).lower() == wlow:
                            idx = i
                            break
                        if str(t).lower() == wlow:
                            idx = i
                            break
                if idx >= 0:
                    cmb.setCurrentIndex(idx)
                else:
                    cmb.setCurrentIndex(0)
            else:
                cmb.setCurrentIndex(0)
        finally:
            cmb.blockSignals(False)

