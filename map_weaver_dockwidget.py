    def _resetGlobeRebuildState(self, clear_pending=True):
        self._globe_morph_count = 0
        self._globe_last_unfold_state = "globe"
        self._globe_last_morph_end_ms_seen = -1.0
        if clear_pending:
            self._globe_rebuild_pending = False

    def _onGlobeUnfoldProgressChanged(self):
        if self._globe_rebuild_in_progress:
            return
        qquick = getattr(self, "_globe_quick_widget", None)
        if qquick is None:
            return
        if not hasattr(self, "cmbGlobeMode") or self.cmbGlobeMode is None:
            return
        if (self.cmbGlobeMode.currentData() or "2d") != "3d_alt":
            return
        root_obj = qquick.rootObject()
        if root_obj is None:
            return
        try:
            u = float(root_obj.property("unfoldProgress"))
        except Exception:
            return
        if u >= 0.98:
            state = "flat"
        elif u <= 0.02:
            state = "globe"
        else:
            state = "mid"
        if state == "flat" and self._globe_last_unfold_state != "flat":
            self._globe_morph_count += 1
            if self._globe_morph_count >= self._globe_rebuild_after_morphs:
                self._globe_rebuild_pending = True
                self._globe_morph_count = 0
        self._globe_last_unfold_state = state
        if self._globe_rebuild_pending and state == "globe":
            self._scheduleGlobeRebuild()

    def _scheduleGlobeRebuild(self):
        if self._globe_rebuild_after_morphs <= 0:
            return
        if self._globe_rebuild_in_progress:
            return
        self._globe_rebuild_in_progress = True
        QtCore.QTimer.singleShot(0, self._rebuildGlobeScene3DAlt)

    def _pollGlobeStateForRebuild(self):
        if self._globe_rebuild_after_morphs <= 0:
            return
        if self._globe_rebuild_in_progress:
            return
        if not hasattr(self, "cmbGlobeMode") or self.cmbGlobeMode is None:
            return
        if (self.cmbGlobeMode.currentData() or "2d") != "3d_alt":
            return
        qquick = getattr(self, "_globe_quick_widget", None)
        if qquick is None:
            return
        root_obj = qquick.rootObject()
        if root_obj is None:
            return
        try:
            morph_anim = bool(root_obj.property("morphAnimating"))
        except Exception:
            morph_anim = False
        try:
            back_pending = bool(root_obj.property("backToGlobePending"))
        except Exception:
            back_pending = False
        try:
            morph_end_ms = float(root_obj.property("morphLastEndMs"))
        except Exception:
            morph_end_ms = -1.0
        if morph_end_ms > 0.0 and morph_end_ms != self._globe_last_morph_end_ms_seen:
            self._globe_last_morph_end_ms_seen = morph_end_ms
            self._globe_morph_count += 1
            if self._globe_morph_count >= self._globe_rebuild_after_morphs:
                self._globe_rebuild_pending = True
                self._globe_morph_count = 0
        try:
            u = float(root_obj.property("unfoldProgress"))
        except Exception:
            return
        if u >= 0.98:
            state = "flat"
        elif u <= 0.02:
            state = "globe"
        else:
            state = "mid"
        if state == "flat" and self._globe_last_unfold_state != "flat":
            self._globe_morph_count += 1
            if self._globe_morph_count >= self._globe_rebuild_after_morphs:
                self._globe_rebuild_pending = True
                self._globe_morph_count = 0
        self._globe_last_unfold_state = state
        # Only rebuild once user is back at globe endpoint; avoid surprising auto-return from flat mode.
        if self._globe_rebuild_pending and not morph_anim and not back_pending and state == "globe":
            self._scheduleGlobeRebuild()

    def _rebuildGlobeScene3DAlt(self):
        try:
            if not hasattr(self, "cmbGlobeMode") or self.cmbGlobeMode is None:
                return
            if (self.cmbGlobeMode.currentData() or "2d") != "3d_alt":
                self._globe_rebuild_pending = False
                return
            self._loadGlobeView("3d_alt")
            self._applyGlobeProjectionToCurrentView()
            self._resetGlobeRebuildState(clear_pending=True)
            QgsMessageLog.logMessage(
                f"Rebuilt Globe Scene3D Alt after {self._globe_rebuild_after_morphs} morph cycles.",
                "MapWeaver Globe",
                Qgis.MessageLevel.Info,
            )
        finally:
            self._globe_rebuild_in_progress = False

    def _loadGlobeView(self, mode):
        self._clearGlobeViewHost()
        if mode != "3d_alt":
            self._globe_rebuild_poll_timer.stop()
            self._resetGlobeRebuildState(clear_pending=True)
        else:
            if self._globe_rebuild_after_morphs > 0 and not self._globe_rebuild_poll_timer.isActive():
                self._globe_rebuild_poll_timer.start()
            elif self._globe_rebuild_after_morphs <= 0 and self._globe_rebuild_poll_timer.isActive():
                self._globe_rebuild_poll_timer.stop()
        if not hasattr(self, "globeViewHostLayout") or self.globeViewHostLayout is None:
            return
        if QQuickWidget is None:
            label = QLabel("QtQuick is unavailable in this QGIS/PyQt build.")
            label.setAlignment(Qt.AlignCenter)
            self.globeViewHostLayout.addWidget(label)
            return

        if mode == "3d":
            qml_name = "globe_view_3d.qml"
        elif mode == "3d_alt":
            qml_name = "globe_view_scene3d.qml"
        elif mode == "fake3d":
            qml_name = "globe_view_fake3d.qml"
        elif mode == "2d":
            qml_name = "globe_view.qml"
        else:
            qml_name = None
        qml_path = os.path.join(os.path.dirname(__file__), qml_name)
        if not os.path.exists(qml_path):
            label = QLabel(f"Globe view file not found: {qml_name}")
            label.setWordWrap(True)
            label.setAlignment(Qt.AlignCenter)
            self.globeViewHostLayout.addWidget(label)
            return
        try:
            qquick = QQuickWidget(self.globeViewHost)
            qquick.setResizeMode(QQuickWidget.SizeRootObjectToView)
            qquick.setClearColor(QtGui.QColor("#ffffff"))
            eng = qquick.engine()
            if eng is not None:
                if getattr(self, "_globe_geo_qml_logger", None) is None:
                    self._globe_geo_qml_logger = GlobeGeoQmlLogger(self)
                eng.rootContext().setContextProperty(
                    "globeGeoLogger", self._globe_geo_qml_logger
                )
                try:
                    eng.clearComponentCache()
                except Exception:
                    pass
            qml_url = QUrl.fromLocalFile(qml_path)
            try:
                mtime_ns = os.path.getmtime_ns(qml_path)
            except Exception:
                mtime_ns = int(os.path.getmtime(qml_path) * 1000000000)
            qml_url.setQuery(f"v={mtime_ns}")
            qquick.setSource(qml_url)
            if qquick.status() == QQuickWidget.Error:
                errs = qquick.errors()
                msg = errs[0].toString() if errs else "Unknown QML load error."
                extra = ""
                if mode == "3d":
                    probe = getattr(self, "_globe_probe", None)
                    if probe and probe.get("3d_msg"):
                        extra = f"\nProbe detail: {probe.get('3d_msg')}"
                label = QLabel(f"Could not load {qml_name}:\n{msg}{extra}")
                label.setWordWrap(True)
                label.setAlignment(Qt.AlignCenter)
                self.globeViewHostLayout.addWidget(label)
                qquick.deleteLater()
                return
            self.globeViewHostLayout.addWidget(qquick)
            self._globe_quick_widget = qquick
            self._globe_qml_path = qml_path
            root_obj = qquick.rootObject()
            if root_obj is not None and qml_name == "globe_view_scene3d.qml":
                env_true = {"1", "true", "yes"}
                dbg = (
                    os.environ.get("MAP_WEAVER_GLOBE_GEO_DEBUG", "").strip().lower() in env_true
                )
                hb = (
                    os.environ.get("MAP_WEAVER_GLOBE_GEO_DEBUG_HEARTBEAT", "").strip().lower()
                    in env_true
                )
                sp = os.environ.get("MAP_WEAVER_GLOBE_GEO_SPIN_PROBE", "").strip().lower() in env_true
                view_seam = os.environ.get(
                    "MAP_WEAVER_GLOBE_VIEW_SEAM_SPLIT", ""
                ).strip().lower() in env_true
                root_obj.setProperty("globeGeoDebugHeartbeat", hb)
                root_obj.setProperty("globeGeoSpinProbeEnabled", sp)
                root_obj.setProperty("globeGeoViewSeamSplitEnabled", view_seam)
                root_obj.setProperty("globeGeoDebug", dbg)
                self._resetGlobeRebuildState(clear_pending=False)
                if dbg:
                    on = []
                    if hb:
                        on.append("heartbeat")
                    if sp:
                        on.append("spin probe")
                    if view_seam:
                        on.append("legacy view seam split")
                    opt = (
                        " Also on: "
                        + ", ".join(on)
                        + "."
                        if on
                        else " Optional: MAP_WEAVER_GLOBE_GEO_DEBUG_HEARTBEAT=1 and/or "
                        "MAP_WEAVER_GLOBE_GEO_SPIN_PROBE=1."
                    )
                    QgsMessageLog.logMessage(
                        "Globe geo debug is ON. Log Messages → “MapWeaver Globe”."
                        + opt
                        + " Env is read at globe load.",
                        "MapWeaver Globe",
                        Qgis.MessageLevel.Info,
                    )
            self._applyGlobeProjectionToCurrentView()
        except Exception as e:
            extra = "\nSwitch to 2D mode if your runtime lacks QtQuick3D." if mode == "3d" else ""
            label = QLabel(f"QtQuick globe unavailable:\n{e}{extra}")
            label.setWordWrap(True)
            label.setAlignment(Qt.AlignCenter)
            self.globeViewHostLayout.addWidget(label)

